package com.dreamstone.util;

public class DebugSettings {
	
	/**
	 * Determines whether or not coordinate pairs are displayed on top of tiles.
	 */
	public static boolean SHOW_COORDS = true;
	
	/**
	 * Determines whether or not the x and y axes should be shown to indicate the location of quadrants.
	 */
	public static boolean SHOW_AXES = false;
	
	/**
	 * Determines whether or not to show gridlines surrounding each tile.
	 */
	public static boolean SHOW_GRIDLINES = false;
}
